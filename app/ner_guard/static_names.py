# -*- coding: utf-8 -*-
"""정적 한국 성명 매트릭스 — PII 이름 탐지용.

설계: docs/design_review_panel_redesign_20260603.md §6.
실측(4000발화): 풀네임 3325개에도 FP 0(이우주·김현정 진짜 포착), 호격만 '소리야'류 소량.
→ 풀네임=자동마스킹(고확신), 호격=검수 플래그.
출처: 통계청 성씨 + 세대별 인기 이름(세계일보/네임차트, 40~60대 영수영숙 ~ 20~30대 민준서연).
"""

# 통계청 성씨 TOP ~35 (인구 약 95% 커버)
SURNAMES = frozenset(
    "김 이 박 최 정 강 조 윤 장 임 한 오 서 신 권 황 안 송 전 홍 "
    "유 고 문 양 손 배 백 허 남 심 노 하 곽 성 차".split()
)

# 세대별 인기 이름 (40~60대 / 30~40대 / 20~30대 통합 ~130)
GIVEN_NAMES = frozenset((
    # 남 (장년~청년)
    "영수 영호 영철 영진 영식 정훈 성호 성수 성민 동현 광수 종수 병철 상철 재현 민수 진우 현우 우진 준호 "
    "지훈 민준 서준 예준 주원 지호 지후 준서 건우 도윤 시우 하준 연우 선우 서진 민재 현준 정우 승우 태윤 "
    "준우 도현 지환 재윤 민성 시윤 윤호 상훈 종현 경수 태현 동건 승현 재호 한결 "
    # 여 (장년~청년)
    "영희 정숙 영숙 순자 영자 미숙 경희 정희 명자 옥자 은주 은정 지영 지혜 민지 유진 수진 미경 현정 영미 "
    "서연 서윤 지우 서현 민서 하은 하윤 윤서 지유 채원 지원 은서 수아 지아 다은 예은 수빈 예린 시은 유나 "
    "채은 지안 하린 수민 가은 다인 소연 예진 지은 혜진 수정 미영 정연 보영 다영 소영 지연 예지 수현 하영 "
    # 추가 고빈도 (지민 등 누락 보강). ※ 일반어 겹침 이름은 제외:
    #   도하(유도하다)·이안(이 안)·시도/인도 등 동사/구문 충돌 → 자동마스킹 FP 유발이라 미포함.
    "지민 서아 시현 주하 라온 하율 시후 지율 서율 윤아 채아 "
    # 중성/겹침위험(호격 저우선이지만 recall 위해 포함)
    "지수 하나 소리 미래 유리 우주 자유 보람 가람 슬기 사랑 한별 다정 새봄 빛나 이슬"
).split())

# 풀네임 = 성+이름 (자동마스킹 후보, FP 0 실측)
FULL_NAMES = frozenset(s + g for s in SURNAMES for g in GIVEN_NAMES)


def _has_jongseong(ch: str) -> bool:
    """한글 음절 받침 유무."""
    return "가" <= ch <= "힣" and (ord(ch) - 0xAC00) % 28 != 0


# 호격 = 이름+아(받침○)/야(받침✕) (검수 플래그 후보, FP 위험 있음)
VOCATIVES = frozenset(
    g + ("아" if _has_jongseong(g[-1]) else "야") for g in GIVEN_NAMES
)
